# append method can add data at the last of the list

fruits=["grapes","apple"]
fruits.append("mango")
print(fruits)

# at any element in any possiion


fruits=["grapes","apple"]
fruits.insert(1,"mango")
print(fruits)

# add two strings

fruits=["grapes","apple"]
fruits1=[1,2,3,4,5,6]
print(fruits+fruits1)

# extend method
fruits1.extend(fruits)
print(fruits1)
